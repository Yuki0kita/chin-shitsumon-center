# 短いカタカナ語の境界チェック対象（例: 「アイヌ」に「イヌ」、「リハーサル」に「サル」を誤検出しない）。
# 3文字以上は「カレーライス」のような複合語への前方一致を許すため対象外
_KATAKANA_ONLY_RE = re.compile(r"^[ァ-ヶー]+$")
_KATAKANA_BOUNDARY_MAX_LEN = 2


def _matches(keyword: str, title: str) -> bool:
    """キーワードが件名に現れるか。短いカタカナ語は前後が非カタカナの場合のみ一致。"""
    if (
        len(keyword) <= _KATAKANA_BOUNDARY_MAX_LEN
        and _KATAKANA_ONLY_RE.match(keyword)
    ):
        pattern = rf"(?<![ァ-ヶー]){re.escape(keyword)}(?![ァ-ヶー])"
        return re.search(pattern, title) is not None
    return keyword in title


def score_question(question: Question) -> int:
    """質問主意書の珍質問度を返す。0未満にはしない。"""
    title = question.title
    if any(t in title for t in _BLOCK_TERMS):
        return 0
    score = 0
    for keyword, weight in _KEYWORD_WEIGHTS.items():
        if _matches(keyword, title):
            score += weight
    return max(score, 0)
